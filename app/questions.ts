// ==================== HELPER FUNCTIONS ====================

// Hàm xáo trộn mảng (Fisher-Yates shuffle)
export const shuffleArray = <T>(array: T[]): T[] => {
  const shuffled = [...array]
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    ;[shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]]
  }
  return shuffled
}

// Hàm lấy ngẫu nhiên n câu từ ngân hàng đề
export const getRandomQuestions = (
  bank: typeof questionsBank,
  count: number = 10
) => {
  const shuffled = shuffleArray(bank)
  return shuffled.slice(0, count)
}

// Hàm xáo trộn đáp án và cập nhật correctIndex
export const shuffleOptions = (question: (typeof questionsBank)[0]) => {
  // Tạo mảng các cặp [option, originalIndex]
  const optionsWithIndex = question.options.map((opt, idx) => ({
    option: opt,
    originalIndex: idx,
  }))

  // Xáo trộn
  const shuffledOptions = shuffleArray(optionsWithIndex)

  // Tìm vị trí mới của đáp án đúng
  const newCorrectIndex = shuffledOptions.findIndex(
    (item) => item.originalIndex === question.correctIndex
  )

  // Trả về câu hỏi mới với đáp án đã xáo trộn
  return {
    ...question,
    options: shuffledOptions.map((item) => item.option),
    correctIndex: newCorrectIndex,
  }
}

// Hàm tạo bộ câu hỏi ngẫu nhiên với đáp án xáo trộn
export const generateQuiz = (bank: typeof questionsBank, count: number = 10) => {
  const randomQuestions = getRandomQuestions(bank, count)
  return randomQuestions.map(shuffleOptions)
}
